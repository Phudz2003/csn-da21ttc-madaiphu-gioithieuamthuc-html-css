  <b>Ảnh hưởng của của ẩm thực Phương Tây</b>
  <p>Đến năm 1859, bánh mì dần trở nên phổ biến trong cộng đồng người Việt. Từ một món ăn đậm vị Pháp, bánh mì được chế biến lại và trở thành một trong những biểu tượng của ẩm thực đường phố Việt Nam. 

Bánh xèo, một loại bánh mỏng với nhân đậu, tôm và thịt heo, cũng bắt nguồn từ bánh crepe của Pháp. Dù có phần tương đồng trong cách chế biến, hai loại bánh trên lại sử dụng những nguyên liệu riêng biệt do bối cảnh địa lý khác nhau.Để tạo nên món bánh mang đặc trưng dân tộc, người Việt đã thay thế bột mì, trứng và sữa bằng những thành phần đơn giản, dễ thấy ở địa phương gồm bột gạo, nước, nghệ. 

Ẩm thực Việt còn trở nên phong phú nhờ sự du nhập của những món ăn phương Tây qua tiến trình toàn cầu hóa. Từ pizza của ẩm thực Ý đến bít tết thuộc ẩm thực Pháp, tất cả đều có thể thưởng thức tại Việt Nam. </p>
